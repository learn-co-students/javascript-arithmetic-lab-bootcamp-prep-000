function add(a, b) {
  return add = a + b;
}
function subtract(a, b) {
  return subtract = a - b;
}
function multiply(a, b) {
  return multiply = a * b;
}
function divide(a, b) {
  return divide = a / b;
}
function inc(n) {
  return inc = n += 1;
}
function dec(n) {
  return dec = n -= 1;
}
function makeInt(n) {
  return parseInt(n, 10);
}
function preserveDecimal(n) {
  return parseFloat(n, 10);
}
