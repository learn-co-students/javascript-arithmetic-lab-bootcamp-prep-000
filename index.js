function add(a,b) {return a+b} //adds a+b

function subtract(b,a) {return b-a} //subtract b-a

function multiply(a,b) {return a*b} //multiply a*b

function divide(a,b) {return a/b} //divides a/b

function inc(n) {return n+1}

function dec(n) {return n-1}

function makeInt(n) {return parseInt(n, 10)}

function preserveDecimal(n) {return parseFloat(n)}
