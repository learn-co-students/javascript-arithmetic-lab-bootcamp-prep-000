function add(a,b) {
  return a+b;
}
add(10,20)
function divide(a,b) {
  return a/b;
}
divide(10,20)

function subtract(a, b) {
  return a - b;
}
subtract(10,20)


function multiply(a, b) {
  return a * b;
}
multiply(10,20)

function inc(n) {
  return ++n;
}
inc(2)

function dec(n) {
  return --n;
}
dec(2)

function makeInt(n) {
  return parseInt(n,10);
}
makeInt('24.2')

function preserveDecimal(n) {
  return parseFloat(n);
}
preserveDecimal(20.3)

