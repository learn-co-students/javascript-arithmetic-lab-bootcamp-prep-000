function add(a, b) {
  a += b
  return a;
}

function subtract(a, b) {
  a -= b
return a;
}

function multiply (a, b) {
  a *= b
return a;
}

function divide(a, b) {
  a /= b
return a;
}

function inc(n) {
  n++
  return n;
}

function dec(n) {
  n--
return n;
}

function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
