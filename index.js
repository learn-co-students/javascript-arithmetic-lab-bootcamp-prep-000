function add(a,b) {
  let sum = a + b 
  return sum
}

function subtract(a,b) {
  let difference = a - b 
  return difference
}

function multiply(a,b) {
  return a * b 
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  n = n + 1
  return n
}

function dec(n) {
  n = n - 1 
  return n
}

function makeInt(n) {
 return parseInt(n,10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}