function add(a,b){
return (a+b)
}

function subtract(a,b){
return (a - b)
}

function multiply(a,b){
return (a*b)
}

function divide(a,b){
return (a/b)
}

function inc(number){
return (++number )
}

function dec(number){
return (--number )
}

function makeInt(number){
return parseInt(number,10)
}

function preserveDecimal(n){
return parseFloat(n)
}
