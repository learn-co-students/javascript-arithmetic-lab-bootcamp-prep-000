function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function makeInt(a) {
  var value = parseInt(a, 10)
  return value
}

function inc(n) {
  return ++n
}

function dec(n) {
  return --n
}

function preserveDecimal(n) {
  var value = parseFloat(n)
  return value
}
