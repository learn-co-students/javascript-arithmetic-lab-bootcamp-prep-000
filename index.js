// add
function add(a, b)
{
  return (a += b);
};
// subtract
function subtract(a, b)
{
  return (a -= b);
};
// multiply
function multiply(a, b)
{
  return (a *= b);
};
// divide
function divide(a, b)
{
  return (a /= b);
};
// inc
function inc(n)
{
  return (n += 1);
};
// dec
function dec(n)
{
  return (n -= 1);
};
// makeInt
function makeInt(n)
{
  return parseInt(n, 10);
};
// preserveDecimal
function preserveDecimal(n)
{
  return parseFloat(n);
};
