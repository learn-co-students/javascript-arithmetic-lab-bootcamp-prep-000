function add(a, b) {
  var result = a + b;
  return result
}
function subtract(a, b) {
  var result = a - b;
  return result
}
function multiply(a, b) {
  var result = a * b
  return result
}
function divide(a, b) {
  var result = a / b
  return result
}
function inc(n) {
  var increment = n += 1;
  return increment
}function dec(n) {
  var decrement = n -= 1;
  return decrement
}
function makeInt(n) {
  var integer = parseInt(n, 10)
  return integer
}
function preserveDecimal(n) {
  var decimal = parseFloat(n)
  return decimal
}