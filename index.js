function add(a,b) {
  return (a + b)
}
function subtract(a,b) {
  return (a - b)
}
function multiply(a,b) {
  return (a * b)
}
function divide (a,b) {
  return (a / b)
}

function increment(a) {
  var number = a + 1
  return number
}
function decrement(a) {
  var number = a - 1
  return number
}
function makeInt(string) {
  var number = parseInt(string, 10)
  return number
}
function preserveDecimal(string) {
  var number = parseFloat(string)
  return number
}
