function add(a,b){
  return a + b
}

function subtract(a,b){
  return a - b
}

function multiply(a,b){
  return a * b
}

function divide(a,b){
  return a / b
}

function inc(a){
  var number = a
  number++
  return number
}

function dec(a){
  var number = a
  number--
  return number
}

function makeInt(a){
  return parseInt(a,10)
}

function preserveDecimal(a){
  return parseFloat(a)
}
