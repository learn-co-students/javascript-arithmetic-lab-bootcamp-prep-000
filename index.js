function add(a, b){
  return parseInt(a + b);
}

function subtract(a,b){
  return parseInt(a - b);
}

function multiply(a,b){
  return parseInt(a * b);
}

function divide(a, b){
  return parseFloat(a / b);
}

function inc(a){
  a++;
  return a;
}

function dec(n){
  n--;
  return n;
}

function makeInt(n){
  return parseInt(n , 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}