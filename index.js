function add(a, b) {
  return parseInt(a, 10) + parseInt(b, 10);
}

function subtract(a, b) {
  return parseInt(a, 10) - parseInt(b, 10);
}

function multiply(a, b) {
  return parseInt(a, 10) * parseInt(b, 10);
}

function divide(a, b) {
  return parseInt(a, 10) / parseInt(b, 10);
}

function inc(n) {
  return n += 1;
}

function dec(n) {
  return n -= 1;
}

function makeInt(a) {
  return parseInt(a, 10);
}

function preserveDecimal(a) {
  return parseFloat(a);
}
