
//add
function add(a, b) {
  return a + b 
}

//subtract
function subtract(a, b) {
  return a - b 
}

//multiply
function multiply(a, b) {
  return a * b
}

//divide
function divide(a, b) {
  return a / b
}


//inc(n)
function inc(n) {
  n++
  return n 
}


//dec(n)
function dec(n) {
  n--
  return n
}


//makeInt(n)
function makeInt(n) {
  parseInt('n', 10)
  return n
}

//makeInt(n) assumes base 10
function makeInt(n) {
   return parseInt(n, 10)
}

//preserveDecimal(n)
function preserveDecimal(n) { 
  return parseFloat(n)
}

