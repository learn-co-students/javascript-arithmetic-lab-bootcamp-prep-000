function add(num1, num2) {
  return num1+num2
}

function subtract(num1, num2) {
  return num1-num2
}

function divide(num1, num2) {
  return num1/num2
}

function multiply(num1, num2) {
  return num1 * num2
}

function inc(n) {
  return n += 1
}

function dec(n) {
  return n -= 1
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
