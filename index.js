//add(a,b)
function add(a, b){
  return a+b
}

//subtract(a,b)
function subtract(a, b){
  return a-b
}

//multiply(a, b)
function multiply(a, b){
  return a*b
}

//divide(a, b)
function divide(a, b){
  return a/b
}

//inc(n)
function inc(n){
  return ++n
}

//dec(n)
function dec(n){
  return --n
}

//makeInt(n)
function makeInt(n){
  return parseInt(n, 10)
}

//preserveDecimal(n)
function preserveDecimal(n){
  return parseFloat(n)
}

