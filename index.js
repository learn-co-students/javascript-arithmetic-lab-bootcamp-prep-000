var a = 1

function add (a, b) {
   return a + b
} 

function subtract (a, b) {
   return a - b
} 

function multiply (a, b) {
  return a * b
}

function divide (a, b) {
  return a / b
}

function inc (n) {
  return n += a
}

function dec (n) {
  return n -= a
}

function makeInt (n) {
  return parseInt (n, 10)
}

function preserveDecimal (n) {
  return parseFloat (n)
}
