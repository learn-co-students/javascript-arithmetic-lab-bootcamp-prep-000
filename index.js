function preserveDecimal(n)
{
  return parseFloat(n,10)
}

function makeInt(n)
{
  return parseFloat(n,10)
}

function dec(n)
{
  return --n
}

function inc(n)
{
  return ++n
}

function divide(a,b)
{
  return a / b
}

function multiply(a,b)
{
  return a *b
}

function add(a,b)
{
  return a + b
}

function subtract(a,b)
{
  return a -b
}