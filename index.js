let add = function(x,y) {
  return x + y;
}

let subtract = function(x,y) {
  return x - y;
}

let multiply = function(x,y) {
  return x * y;
}

let divide = function (x,y) {
  return x / y;
}

let dec = function(n) {
  return --n;
}

let inc = function(n) {
  return ++n;
}

let makeInt = function(n) {
  return parseInt(n, 10);
}

let preserveDecimal = function(n) {
  return parseFloat(n);
}
