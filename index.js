
function add(a,b){
  return a += b
}

function subtract(a,b){
  return a -= b
}

function multiply(a,b){
  return a *= b
}

function divide(a,b){
  return a /=b
}

function makeInt(num){
  return parseInt(num)
}

function preserveDecimal(num){
  return parseFloat(num)
}

function inc(n){
  n++
  return n
}

function dec(n){
  n--
  return n
}

function makeInt(string){
  return parseInt(string, 10)
}
