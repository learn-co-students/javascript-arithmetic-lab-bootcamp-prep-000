function add(a,b) {
  var sum = a + b
  return sum
}

function subtract(a,b) {
  var difference = a - b
  return difference
}

function multiply(a,b) {
  var product = a * b
  return product
}

function divide(a,b) {
  var quotient = a / b
  return quotient
}

function inc(n) {
  n+=1
  return n
}

function dec(n) {
  n-=1
  return n
}

function makeInt(n) {
  n = parseInt(n,10)
  return n
}

function preserveDecimal(n){
  n = parseFloat(n)
  return n
}
