function add (a,b){
  return a + b;
}

function subtract (a, b){
  return a - b;
}

function multiply (a, b) {
  return a * b;
}

function divide (a, b) {
  return a / b;
}

function inc (a) {
  return a + 1;
}

function dec (a) {
  return a - 1;
}

function makeInt(n, base){
  var number = parseInt(n, 10);
  return number
}

function preserveDecimal (n){
  var decimal = parseFloat(n);
  return decimal
}

