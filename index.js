function add(a, b) {
  return (a + b);
}

function subtract(b, a) {
  return (b - a);
}

function multiply(a, b) {
  return (a * b);
}

function divide(a, b) {
  return (a / b);
}

function inc(a) {
  return ++a
}

function dec(a) {
  return --a
}

function makeInt(a) {
  return parseInt(a, 10)
}

function preserveDecimal(a) {
  return parseFloat(a)
}
