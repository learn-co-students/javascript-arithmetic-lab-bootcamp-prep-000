function add(a,b) {
  var number = a + b
  return number
}

function subtract(a,b) {
  var number = a - b
  return number
}

function multiply(a,b) {
  var number = a * b
  return number
}

function divide(a,b) {
  var number = a / b
  return number
}

function inc(n) {
  n += 1
  return n
}

function dec(n) {
  n -= 1
  return n
}

function makeInt(n) {
  var number = parseInt(n, 10)
  return number
}

function preserveDecimal(n) {
  var number = parseFloat(n)
  return number
}
