function add(a, b) {
  var number = a + b;
  return number;
}

function subtract(a, b) {
  var number = a - b;
  return number;
}

function multiply(a, b) {
  var number = a * b;
  return number;
}

function divide(a, b) {
  var number = a / b;
  return number;
}

function inc(n) {
  n++
  var number = n;
  return number;
}

function dec(n) {
  n--
  var number = n;
  return number;
}

function makeInt(n) {
  var number = parseInt(n, 10);
  return number;
}

function preserveDecimal(n) {
  var number = parseFloat(n, 10);
  return number;
}
