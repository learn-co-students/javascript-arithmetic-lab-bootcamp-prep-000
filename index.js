function add(a, b) {
  return a + b
}

function multiply(a, b) {
  return a * b
}

function inc(n) {
  n++
  return n
}

function subtract(a, b) {
  return a - b
}

function divide(a, b) {
  return a / b
}

function dec(n) {
  n --
  return n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}