function add(num1, num2){
return num1+num2;
}

function subtract(num1, num2){
  return num1-num2;
}

function multiply(num1, num2){
  return num1*num2;
}

function divide(num1, num2){
  return num1/num2;
}

function inc(number){
  return number+=1;
}

function dec(number){
  return number-=1;
}

function makeInt(arg){
  return parseInt(arg, 10);
}

function preserveDecimal(arg){
  return parseFloat(arg);
}
