function add(a, b) { //adds two numbers and returns the result', function() {
  return a + b;
}

function subtract(a, b) { //subtracts b from a and returns the result', function() {
  return a - b;
}

function multiply(a, b) { //multiplies two numbers and returns the result', function() {
  return a * b;
}

function divide(a, b) { // divides a by b and returns the result', function() {
  return a / b;
}

function inc(n) { // increments n and returns the result', function() {
  return ++n;
}

function dec(n) { // decrements n and returns the result', function() {
  return --n;
}

function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n, 10);
}
