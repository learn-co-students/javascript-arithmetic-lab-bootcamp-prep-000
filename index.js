var c;
function add(a, b){
  c = a + b;
  return c;
}
function subtract(a, b){
  c = a - b;
  return c;
}
function multiply(a, b){
  c = a* b;
  return c;
}
function divide(a, b){
  c = a/b;
  return c;
  
}
function inc(a){
  c = a+1;
  return c;
}

function dec(a){ 
  c = a-1;
  return c;
}
function makeInt(a){ 
  c = parseInt(a,10);
  return c;
}
function preserveDecimal(a){  
  c = parseFloat(a, 10);
  return c;
}

