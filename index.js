function add(x,y) {
  return x + y
}
function subtract(x,y) {
  return x - y
}
function multiply(x,y) {
  return x * y
}
function divide(x,y) {
  return x / y
}
function inc(n) {
  var number = n
  return number += 1
}
function dec(n) {
  var number = n
  return number -= 1
}
function makeInt(n) {
  var number = n
  return parseInt (n,10)
}
function preserveDecimal(n) {
  var number = n
  return parseFloat (n)
}
