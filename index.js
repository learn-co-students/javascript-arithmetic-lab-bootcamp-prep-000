//add
function add(a,b) {
  return a + b
}

//subtract
function subtract(a,b){
  return a - b 
}

//multiply
function multiply(a,b) {
  return a * b 
}

//divide
function divide(a,b) {
  return a / b 
}

//increment
function inc(n) {
  return n + 1
}

//decrement
function dec(n) {
  return n - 1
}

//make integer
function makeInt(n){
  return parseInt(n,10)
}

//make decimal
function preserveDecimal(n) {
  return parseFloat(n)
}