function add(a,b)
{
  var sum = a + b
  return sum
}

function subtract(a,b)
{
var difference = a - b
return difference
}

function multiply(a,b)
{
var product = a * b
return product
}

function divide(a,b,)
{
var divide = a / b
return divide
}

function inc(a)
{
return a+=1
}

function dec(a)
{
return a-=1
}

function makeInt(a)
{
var int = parseInt(a,10)
return int
}

function preserveDecimal(a)
{
var float = parseFloat(a)
return float
}
