// Add
function add(a, b) {
  return a + b
}
// Subtract
function subtract(a, b) {
  return a - b
}
// Multiply
function multiply(a, b) {
  return a * b
}
// Divide
function divide(a, b) {
  return a / b
}
// Increment
function inc(n) {
  return n += 1
}
// Decrement
function dec(n) {
  return n -= 1
}
// Make Integer
function makeInt(n) {
  return parseInt(n, 10)
}
// Preserve Decimal
function preserveDecimal(n) {
  return parseFloat(n)
}