function add (a, b) {
  var total
  total = a + b 
  return total
}

function subtract (a, b) {
  var total
  total = a - b
  return total
}

function multiply (a, b) {
  var total
  total = a * b 
  return total 
}

function divide (a, b) {
  var total 
  total = a / b 
  return total
}

function inc (a) {
  a++
  return a
}

function dec (a) {
  a-- 
  return a 
}

function makeInt (n) {
  return parseInt (n,10)
}

function preserveDecimal (n) {
  return parseFloat (n)
}