function add(n1, n2) {
  return n1 + n2
}

function subtract(nu1, nu2) {
  return nu1 - nu2
}

function multiply(num1, num2) {
  return num1 * num2
}

function divide(numb1, numb2) {
  return numb1 / numb2
}

function inc(a) {
  return a + 1
}

function dec(n) {
  return n - 1
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
