function preserveDecimal(n){
  var float = parseFloat(n);
  return float;
}

function makeInt(n){
  var int = parseInt(n, 10);
  return int;
}

function inc(n){
  n++;
  return n;
}

function dec(n){
  n--;
  return n;
}

function multiply(a,b){
  return (a*b);
}
function divide(a,b){
  return(a/b);
}
function add(a,b){
  return (a + b);
}
function subtract(a,b){
  return(a - b);
}