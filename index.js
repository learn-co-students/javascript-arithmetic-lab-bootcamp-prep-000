function add(a,b) {
  return a+b
}

function subtract(a,b) {
  return a-b
}

function multiply(a,b) {
  return a*b
}

function divide(a,b) {
  return a/b
}

function inc(n) {
  return n+=1
}

function dec(n) {
  return n-=1
}

function makeInt(n) {
  var Int = parseInt(n,10)
  return Int
}

function preserveDecimal(n) {
  var Dec = parseFloat(n,10)
  return Dec
}