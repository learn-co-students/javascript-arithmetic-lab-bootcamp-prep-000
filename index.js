function add(a,b) {
   return a + b;
}

function subtract(a,b) {
   return a - b;
}

function multiply(a,b) {
   return a * b;
}

function divide(a,b) {
  return a / b;
}

function inc(int) {
     int++;
     return int;
}

function dec(int) {
  var number = int;

  number--;
  return number;
}

function makeInt(n) {
  return parseInt(n,10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
