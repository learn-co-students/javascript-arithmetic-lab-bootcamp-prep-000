function add(x,y) {
  return x + y
}

function subtract(x,y) {
  return x - y
}

function multiply(x,y) {
  return x * y
}

function divide(x,y) {
  return x / y
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  var integer = parseInt(n, 10);
  return integer
}

function preserveDecimal(n) {
  var float = parseFloat(n);
  return float
}
