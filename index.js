function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(a) {
  return ++a
}

function dec(a) {
  return --a
}

function makeInt(num) {
  return parseInt(num, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
