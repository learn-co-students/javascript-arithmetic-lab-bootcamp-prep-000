function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  
  return a * b;
}


function divide(a, b) {
  return a/b ;
}

function inc(c) {
   c++;
   return c;
}

function dec(c) {
  c--;
  return c;
}

function makeInt(n) {
  return parseInt(n, 10);
  
}

function preserveDecimal (n) {
  return parseFloat(n);
}