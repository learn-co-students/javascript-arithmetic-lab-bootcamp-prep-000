function add(num1, num2) {
  return num1+num2

}

function subtract(num1, num2) {
  return num1-num2

}

function multiply(num1, num2) {
  return num1*num2

}

function divide(num1, num2) {
  return num1/num2

}

function inc (num){
  return num +=1
  
}

function dec (num){
  return num -=1
  
}

function makeInt(num_str) {
  return parseInt(num_str)
}
 
function preserveDecimal(num_str) {
  return parseFloat(num_str.toString())
  
}