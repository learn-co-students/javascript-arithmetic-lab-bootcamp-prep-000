function add(a,b){
  return a+b
}
//subtract
function subtract(a,b){
  return a-b
}
//multiply
function multiply(a,b){
  return a*b
}
//divide
function divide(a,b){
  return a/b
}
function inc(a){
  return a+1
}
function dec(a){
  return a-1
}
function makeInt(a){
  return parseInt(a, 10)
}
function preserveDecimal(a){
  return parseFloat(a)
}
