function add(a, b) {
   var addTotal = a + b
 return addTotal
  }
  
function subtract(a, b) {
   var subtractTotal = a - b
 return subtractTotal
  }
  
function multiply(a, b) {
   var multiplyTotal = a * b
 return multiplyTotal
  }
  
function divide(a, b) {
   var divideTotal = a / b
 return divideTotal
  }
  
function inc(a) {
   var incTotal = a + 1
 return incTotal
  }

function dec(a) {
   var decTotal = a - 1
 return decTotal
  }

