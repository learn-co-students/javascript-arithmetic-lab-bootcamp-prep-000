function add(a, b) {
  add = a + b;
  return add
}

function subtract(a, b) {
  subtract = a - b;
  return subtract
}

function multiply(a, b) {
  multiply = a * b;
  return multiply
}

function divide(a, b) {
  divide = a / b;
  return divide
}

function inc(n) {
  return n += 1
}

function dec(n) {
  return n -= 1
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n, 10)
}
