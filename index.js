//add
function add(a, b) {
  return (a + b)
}
//subtract
function subtract(a, b) {
  return (a - b)
}
//multiply
function multiply(a, b) {
  return (a * b)
}
//divide
function divide(a, b) {
  return (a / b)
}
//inc(n)
function inc(n) {
  return (n += 1)
}
//dec(n)
function dec(n) {
  return (n -= 1)
}
//makeInt(n)
function makeInt(n){
  return parseInt(n, 10)
}
//preserveDecimal(n)
function preserveDecimal(n){
  return parseFloat(n)
}
