// adding 2 numbers together
function add(a, b){
  return a + b;
}

// subtracting 2 numbers
function subtract(a, b){
  return a - b;
}

// multiplying 2 numbers
function multiply(a, b){
  return a * b;
}

// divide 2 numbers
function divide(a, b){
  return a / b;
}

// increments a numbers
function inc(n){
  return n + 1;
}

// decrement a number
function dec(n){
  return n - 1;
}

var number

function makeInt(n){ 
  number = parseInt(n, 10);
  return number;
}
function preserveDecimal(n){ 
  number = parseFloat(n, 10);
  return number;
}
