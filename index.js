function add(a) {
  a += a
}

function subtract(a) {
  a -= a
}

function multiply(a) {
  a *= a
}

function divide(a) {
  a /= a
}

function add(a,b) {
  return a += b
}

function subtract(a,b) {
return  a -= b
}

function multiply(a,b) {
return  a *= b
}

function divide(a,b) {
return  a /= b
}

function increment(n) {
return  n += 1
}

function decrement(n) {
return  n -= 1
}

function makeInt(n) {
  return parseInt(n,10)  
}

function preserveDecimal(n) {
  return parseFloat(n)
}
