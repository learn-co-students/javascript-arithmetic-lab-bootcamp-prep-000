// Function that adds to numbers, a and b.
function add(a, b) {
  return a + b;
}

// Function that subtracts one number, b, from another number, a.
function subtract(a, b) {
  return a - b;
}

// Function to find product of two numbers, a and b.
function multiply(a, b) {
  return a * b;
}

// Function that divides one number, a, by another number, b.
function divide(a, b) {
  return a / b;
}

// Function to increment a number, n, by 1.
function inc(n) {
  return n += 1;
}

// Function to decrement a number, n, by 1.
function dec(n) {
  return n -= 1;
}

// Function to pass an integer, n, and returns parsed integer.
function makeInt(n) {
  return parseInt(n, 10);
}

// Function to preserve floating point number, n, and returns FP.
function preserveDecimal(n) {
  return parseFloat(n);
}
