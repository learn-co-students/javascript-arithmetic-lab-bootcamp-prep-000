function add( a, b){
  a+b
  return a + b
}

function subtract(a, b){
  a - b
  return a-b
}

function multiply(a, b){
  a*b
  return a*b
}

function divide(a, b){
  a/b 
  return a/b 
}

function inc(n){
  n++
  return n++
}

function dec(n){
  n--  
  return n-- 
}

function makeInt(n){
  parseInt(n , 10)
  return parseInt(n, 10)
}

function preserveDecimal(n){
  parseFloat(n)
  return parseFloat(n)
}