function add(x, y){
  return x + y
}

function subtract(x, y){
  return x - y
}

function multiply(x, y){
  return x * y
}

function divide(x, y){
  return x / y
}

function inc(n){
  return ++n
}

function dec(n){
  return --n
}

function makeInt(n){
  return parseFloat(n)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
