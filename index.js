function add(a,b) {
  return a + b
}

function subtract(a,b) {
  return a - b
}

function multiply(a,b) {
  return a * b
}

function divide(a,b) {
  return a / b
}

function increment(a) {
  a += 1
  return a
}

function decrement(a) {
  a -= 1
  return a
}

function makeInt(a) {
  return parseInt(a, 10)
}

function preserveDecimal(a) {
  return parseFloat(a, 10)
}

