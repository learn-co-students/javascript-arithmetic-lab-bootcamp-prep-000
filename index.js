function add(a, b){
  var total = a + b
  return total
}

function subtract(a, b){
  var total = a - b
  return total
}

function multiply(a, b){
  var total = a * b
  return total
}

function divide(a, b){
  var total = a / b
  return total
}

function inc(n){
  n += 1
  return n
}

function dec(n){
  n -= 1
  return n
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}
