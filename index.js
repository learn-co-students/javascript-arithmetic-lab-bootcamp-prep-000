function add (num1, num2)
{
  return num1 + num2
}

function subtract (num1, num2)
{
  return num1 - num2
}

function multiply (num1, num2)
{
  return num1 * num2
}

function divide (num1, num2)
{
  return num1 / num2
}

function inc (num)
{
  return num = num + 1
}

function dec (num)
{
  return num = num - 1
}

function makeInt(num)
{
  return parseInt(num, 10)
}

function preserveDecimal (num)
{
  return parseFloat (num, 10)
}
