function add(a,b){
  var sum = a
  sum +=b
return sum
  }

function subtract(a,b) {
  var diff = a
  diff -=b
  return diff
}

function multiply (a,b) {
var prod = a
prod *= b
return prod
}

function divide(a,b) {
  var quot = a
quot /= b
  return quot
}

function inc (n) {
  var i = n
  i++
  return i
}

function dec (n) {
  var i = n
  i--
  return i
}

function makeInt (n) {
  var int = parseInt(n,10)
  return int
}

function preserveDecimal (n) {
  var i = parseFloat(n)
return i
}
