function add(n, m){
  return n+m;
}

function subtract(n, m){
  return n-m;
}

function multiply(n,m){
  return n*m;
}

function divide(n,m){
  return n/m;
}

function inc(n){
  return ++n;
}

function dec(n){
  return --n;
}

function makeInt(n){
  return parseInt(n, 10);
}

function preserveDecimal(n){
  return parseFloat(n);
}