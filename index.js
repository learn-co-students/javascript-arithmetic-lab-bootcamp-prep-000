function add(a,b) {
  var sum = a + b;
  return sum
}

function subtract(a,b) {
  var minus = a - b;
  return minus
}

function multiply(a,b) {
  var mult = a*b;
  return mult
}

function divide(a,b) {
  var div = a/b;
  return div
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  return parseInt(n,10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
