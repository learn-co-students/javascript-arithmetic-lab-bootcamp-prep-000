function add(a, b){ 
  return parseInt(a + b) 
}

function subtract (a,b){
  return parseInt(a - b) 
}

function multiply(a, b){
  return parseInt(a * b) 
}

function divide (a,b){
  return parseFloat(a / b) 
}

function inc(a){
  a++
  return a
}

function dec(a){
  a--
  return a
  }

