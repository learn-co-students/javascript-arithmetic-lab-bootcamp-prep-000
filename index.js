function add(a,b){
  return a+b
}

function subtract(a,b){
  var result = a - b
  return result
}

function multiply(a,b){
  var result = a*b
  return result
}

function divide(a,b){
  var result = a / b
  return result
}

function inc(n){
  var result = ++n
  return result
}

function dec(n){
  var result = -- n
  return result
}

function makeInt(n){
var integer = parseInt (n , 10)
return integer
}

function preserveDecimal(n){
  var decimal= parseFloat (n)
  return decimal
}
