function add(a,b){
  a  = a + b
  return a
}
function subtract(a,b){
  a = a-b
  return a
}
function multiply(a,b){
  a = a*b
  return a
}
function divide(a,b){
  a = a/b
  return a
}
function inc(n){
  n = n + 1
  return n
}
function dec(n){
  n = n - 1
  return n
}
function makeInt(n){
  n = parseInt(n,10)
  return n
}
function preserveDecimal(n){
  n = parseFloat(n)
  return n
}
