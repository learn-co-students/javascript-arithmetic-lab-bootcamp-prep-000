function add(a,b){
  return a+b;
}
function subtract(a,b){
  a= a-b;
  return a;
}
function multiply(a,b){
  return a*b;
}
function divide(a,b){
  return a/b;
}
function inc(a){
  return ++a;
}
function dec(a){
  return --a;
}
function makeInt(a){
  return parseInt(a, 10);
}
function preserveDecimal(b){
  return parseFloat(b);
}