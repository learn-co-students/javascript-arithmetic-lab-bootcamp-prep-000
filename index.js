function add(a,b) {
  var addition = a + b
  return addition
}

function subtract(a,b) {
  var subtraction = a - b
  return subtraction
}

function multiply(a,b) {
  var multiplcation = a*b
  return multiplcation
}

function divide(a,b) {
  var division = a/b
  return division
}

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  var integer = parseInt(n,10)
  return integer
}

function preserveDecimal(n) {
  var float = parseFloat(n)
  return float
}