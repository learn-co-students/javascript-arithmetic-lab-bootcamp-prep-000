function add(num1, num2) {
  return (num1 + num2)
}

function subtract(a, b) {
  return (a - b)
}

function multiply(c, d) {
  return (c * d)
}

function divide(z, x) {
  return (z / x)
}

function inc(n) {
  return ++n
}

function dec(n) {
  return --n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
