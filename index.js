function add(a,b){
  return a + b
}

function subtract(a,b){
  return a - b
}

function multiply(a,b){
  return a*b
}

function divide(a,b){
  return a/b
}

function dec(a){
  a--
  return a
}

function inc(a){
  a++
  return a
}

function makeInt(n){
  n=parseInt(n,10)
  return n
}

function preserveDecimal(n){
  n=parseFloat(n,10)
  return n
}
