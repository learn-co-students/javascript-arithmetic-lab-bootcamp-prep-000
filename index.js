var a, b, c, n
function add(a,b) {
  c = a + b
  return c
}

function subtract(a,b) {
  c = a - b
  return c
}

function multiply(a,b) {
  c = a*b
  return c
}

function divide(a,b) {
  c = a/b
  return c
}


function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n) {
  parseInt(n)
  return n
}

function preserveDecimal(n) {
  parseFloat(n)
  return n
}