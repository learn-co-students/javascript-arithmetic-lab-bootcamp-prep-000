function add(a,b){
  var result
  return result = a + b
}

function subtract(a,b){
  var result
  return result = a - b
}

function multiply(a,b){
  var result
  return result = a * b
}

function divide(a,b){
  var result
  return result = a / b
}

function inc(n){
  n++
  return n
}
function dec(n){
  n--
  return n
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}
