function add (a, b){
  return (a + b)
}

function subtract (a, b){
  return (a - b)
}

function multiply (a, b){
  return (a * b)
}

function divide (a, b ){
  return (a / b)
}
function inc (a){
  return (a + 1)
}

function dec(a){
  return (a - 1)
}

function makeInt (a){
  var parsed = parseInt(a);
  return parsed

}

function makeInt(n){
  return parseInt(n, 10)
  return parseInt('n', 10)
}

function preserveDecimal(n){
  return parseFloat(n)
  return parseFloat('n', 10)
}
