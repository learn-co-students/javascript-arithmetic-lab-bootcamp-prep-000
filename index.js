function add(a, b){
  return a + b
}
function subtract(a, b){
  return a - b
}
function multiply(a, b){
  return a * b
}
function divide(a, b){
  return a / b
}
function inc(a){
  return a + 1
}
function dec(a){
  return a - 1
}
function makeInt(a, isNaN){
  return parseInt(a, 10)
  return parseInt(isNaN, 10)
}
function preserveDecimal(isNaN){
  //return parseFloat('2.222')
  return parseFloat(isNaN)
}
