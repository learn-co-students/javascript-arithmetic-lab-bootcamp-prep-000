function add(a, b) {
  var answer = a + b
  return answer
}

function subtract(a, b) {
  var answer = a - b
  return answer
}

function multiply(a, b) {
  var answer = a * b
  return answer
}

function divide(a, b) {
  var answer = a / b
  return answer
}

function inc(n) {
  var answer = ++n
  return answer
}

function dec(n) {
  var answer = --n
  return answer
}

function makeInt(n) {
  var answer = parseInt(n, 10)
  return answer
}

function preserveDecimal(n) {
  var answer = parseFloat(n)
  return answer
}
