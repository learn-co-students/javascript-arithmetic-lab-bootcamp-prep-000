function add(a,b){
  a += b
  return a
}

function subtract(a,b){
  a -= b
  return a
}

function multiply(a,b){
  a *= b
  return a
}

function divide(a,b){
  a /= b 
  return a
}

function increment(n){
  n++
  return n
}

function decrement(n){
  n--
  return n
}

function makeInt(n){
  return parseInt(n, 10)
  
}

function preserveDecimal(n){
  return parseFloat(n)
 
}