// Adds two numbers
function add(a, b) {
  return a + b
}

// Subtracts two numbers
function subtract(a, b) {
  return a - b
}

// Multiplies two numbers
function multiply(a, b) {
  return a * b
}

// Divides two numbers
function divide(a, b) {
  return a / b
}

// Increases n by 1
function inc(n) {
  return n += 1
}

// Decreases n by 1
function dec(n) {
  return n -= 1
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
