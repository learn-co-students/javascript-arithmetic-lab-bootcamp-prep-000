function add(a, b){
   return a + b
}

function subtract(a,b){
  return a - b  
}

function multiply(a,b){
  return a * b  
}

function divide(a,b){
  return a / b  
}

function inc(a,b){
  return a + 1  
}


function dec(a,b){
  return a - 1  
}

function makeInt(a,b){
  return parseInt(a, 10)  
}

function preserveDecimal(a,b){
  return parseFloat(a, 10)  
}