function add(int1,int2){
  return int1+int2
}
function subtract(int1,int2){
  return int1-int2
}
function multiply(int1,int2){
  return int1*int2
}
function divide(int1,int2){
  return int1/int2
}
function inc(int){
  return int+1
}
function dec(int){
  return int-1
}
