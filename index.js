function add(a,b){
  return Number(a) + Number(b);
}

function subtract(a, b){
  return Number(a) - Number(b);
}

function multiply(a,b){
  return Number(a) * Number(b);
}

function divide(a,b){
  return Number(a) / Number(b);
}

function inc(a){
  return Number(a) + 1;
}

function dec(a){
  return Number(a) - 1;
}

function makeInt(a){
  return parseInt(a, 10);
}

function preserveDecimal(a){
  return parseFloat(a, 10);
}
