function add(a,b){
  return a + b;
}

function subtract(a,b){
  return a - b;
}
function multiplies(a,b){
  return a * b;
}
function divides(a,b){
  return a / b;
}
function inc(a,b){
  return a++;
}
function dec(a,b){
  return a--;
}
function makeInt(a){
  return parseInt(a, 10);
}
function preserveDecimal(a){
  return parseFloat(a);
}
