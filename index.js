function add(x,y) {
  return x+y
}
function subtract(x,y) {
  return x-y
}
function multiply(x,y) {
  return x*y
}
function divide(x,y) {
  return x/y
}

var n = 0

function inc(n) {
  n+= 1
  return n
}

function dec(n) {
  n-= 1
  return n
}

function makeInt(n) {
  parseInt(n)
  return n
}

function preserveDecimal(n) {
  parseFloat(n)
  return n
}
