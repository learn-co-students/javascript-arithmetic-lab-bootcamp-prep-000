function add(a,b){
  var result = a + b
  return result
}

function subtract(a,b){
  var result = a - b 
  return result
}

function multiply(a,b){
  var product = a * b 
  return product
}

function divide(a,b){
  var quotient = a/b
  return quotient
}

function inc(n){
  return n + 1
}

function dec(n){
  return n - 1
}

function makeInt(n){
  var makeInt = parseInt(n,10)
  return makeInt
}

function preserveDecimal(n){
  var decimals = parseFloat(n)
  return decimals
}