function add(a,b){
  var result = a + b
  return result
}

function subtract(a,b){
  var result = a - b
  return result
}

function multiply(a,b){
  var result = a * b
  return result
}

function divide(a,b){
  var result = a / b
  return result
}

function inc(n){
  n += 1
  return n
}

function dec(n){
  n -= 1
  return n
}

function makeInt(n){
  var result = parseInt(n,10)
  return result
}

function preserveDecimal(n){
  var result = parseFloat(n)
  return result
}
