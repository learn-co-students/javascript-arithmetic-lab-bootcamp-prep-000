function add(num1, num2)
{
  var sum = num1 + num2;
  return sum;
}

function subtract(num1, num2)
{
  var sum = num1 - num2;
  return sum;
}

function multiply(num1, num2)
{
  var sum = num1 * num2;
  return sum;
}

function divide(num1, num2)
{
  var sum = num1 / num2;
  return sum;
}

function inc(n)
{
  return n += 1; 
}

function dec(n)
{
  return n -= 1; 
}

function makeInt(n)
{
  return parseInt(n, 10);
}

function preserveDecimal(n)
{
  return parseFloat(n);
}







