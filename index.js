






function add(n,nn) {
  return parseFloat(n)+parseFloat(nn)
}
function subtract(n,nn) {
  return parseFloat(n)-parseFloat(nn)
}
function multiply(n,nn) {
  return parseFloat(n)*parseFloat(nn)
}
function divide(n,nn) {
  return parseFloat(n)/parseFloat(nn)
}
function inc(n) {
  return parseFloat(n)+1
}
function dec(n) {
  return parseFloat(n)-1
}
function makeInt(n) {
  return parseInt(n,10)
}
function preserveDecimal(n) {
  return parseFloat(n)
}