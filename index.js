function add(x,y){
  return x + y;
}
function subtract(a,b){
  return a - b;
}
function multiply(a,b){
  return a * b;
}
function divide(a,b){
  return a / b;
}
function inc(a){
  return ++a;
}
function dec(n){
  return --n;
}
function makeInt(n){
  return parseInt(n, 10);
}
function preserveDecimal(n){
  return parseFloat(n);
}