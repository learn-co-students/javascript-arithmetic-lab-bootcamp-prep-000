function add(lhs, rhs) {
  return lhs + rhs;
}

function subtract(lhs, rhs) {
  return lhs - rhs;
}

function multiply(lhs, rhs) {
  return lhs * rhs;
}

function divide(lhs, rhs) {
  return lhs / rhs;
}

function inc(n) {
  n++;
  return n;
}

function dec(n) {
  n--;
  return n;
}

function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}