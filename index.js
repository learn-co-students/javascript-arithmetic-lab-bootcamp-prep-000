function add(a,b) {
  return a + b //next 4 functions are simple arithmetic
}
function subtract(a, b){
  return a - b
}
function multiply(a,b){
  return a*b
}
function divide(a,b){
  return a/b
}
function inc(n){
  return ++n //increase to get one more than original number
}

function dec(n){
  return --n //decend to get one less than the original number
}

function makeInt(n){
  return parseInt(n,10)
}
function preserveDecimal(n){
  return parseFloat(n,10)
}
