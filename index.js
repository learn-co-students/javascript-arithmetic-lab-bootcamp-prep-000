function add(a, b){
  return a+b;
}
function subtract(a, b){
  return a-b;
}
function multiply(a, b){
  return a*b;
}
function divide(a, b){
  return a/b;
}
function inc(a){
  return a+1;
}
function dec(a){
  return a-1;
}

function makeInt(n){
  var x = parseInt(n,10);
  return x;
}

function preserveDecimal(n){
  var y = parseFloat(n);
  return y;
}
