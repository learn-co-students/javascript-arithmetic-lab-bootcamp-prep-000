//add
function add(a, b) {
  return a + b
}

//subtract (a,b)
function subtract(a, b) {
  return a - b
}

//multiply (a,b)
function multiply(a, b) {
  return a * b
}

//divide (a,b)
function divide(a, b) {
  return a / b
}

function inc(n) {
  n++
  return n 
}

//decrement
function dec(n) {
  n--
  return n
}

//makeInt
function makeInt(n) {
  return parseInt(n, 10)
}

//preserveDecimal(n)
function preserveDecimal(n) {
  return parseFloat(n)
}