function add(x, y){
  return x+y
}

function subtract(x, y){
  return x-y
}

function multiply(x, y){
  return x*y
}

function divide(x, y){
  return x/y
}

function inc(n){
  return ++n
}

function dec(n){
  return --n
}

function makeInt(n){
  var x = parseInt(n, 10)
  return x
}

function preserveDecimal(n){
  var x = parseFloat(n)
  return x
}
