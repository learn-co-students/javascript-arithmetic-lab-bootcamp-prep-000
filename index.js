function add(a,b){
  return a + b
} //add two numbers and returns the result

function subtract(a,b){
  return a - b
}//subtract two numbers and returns the result

function multiply(a,b){
  return a * b
}//multiply two numbers and returns the result

function divide(a,b){
  return a / b
}//divide two numbers and returns the result

function inc(n){
  return ++n
}//increments n and return the result

function dec(n){
  return --n
}//decrements n and return the result

function makeInt(n){
  return parseInt(n, 10)
}//parses n as an integer and returns the parsed integer

function preserveDecimal(n){
  return parseFloat(n, 10)
}//parses n as a floating point number and returns the parsed number
