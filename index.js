function add (a,b) {
  return a + b
}

function subtract(a,b) {
  return a - b
}

function multiply (a,b) {
  return a * b
}

function divide (a,b) {
  return a / b
}

function inc (num) {
  return num += 1
}

function dec (num) {
  return num -= 1
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
