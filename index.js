function add(n1,n2){
  return n1 + n2;
}
function subtract(a,b){
  return a-b;
}
function multiply(a,b){
  return a*b;
}
function divide(a,b){
  return a/b;
}
function makeInt(n){
  return parseInt(n,10);
}
function inc(n){
  return ++n;
}
function dec(n){
  return --n;
}
function preserveDecimal(n){
  return parseFloat(n);
}
