
function add(a, b){
  return (a + b)
}

function subtract(a, b){
  return (a - b)
}

function multiply(a, b){
  return (a * b)
}

function divide(a, b){
  return (a / b)
}

function inc(a){
  a += 1
  return (a)
}

function dec(a){
  a -= 1
  return (a)
}

function makeInt(n, a){
  n = parseInt(n, 10)
  parseInt(a, 10)
  return (a, n)
}

function preserveDecimal(n, a){
  n = parseFloat(n)
  parseFloat(a)
  return (a, n)
}