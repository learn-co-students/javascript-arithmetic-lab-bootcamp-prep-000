function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
  var n
  n += 1
  return n
}

function dec(n) {
  var n
  n -= 1
  return n
}

function makeInt(n) {
  var n
  parseInt(n, 10)
  return n
}

function makeInt(n) {
  parseInt('n', 10)
  return n
}

function preserveDecimal(n) {
  parseFloat(n)
  return n
}