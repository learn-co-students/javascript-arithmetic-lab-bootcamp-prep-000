function add(a, b) {
  return a + b
}

add(2, 3)

function subtract(a, b) {
  return a - b
}

subtract(5, 4)

function multiply(a, b) {
  return a * b
}

multiply(4, 5)

function divide(a, b) {
  return a / b
}

