// Adds two integers.
function add(x,y) {
  return x + y
}

// subtracts two integers.
function subtract(x,y) {
  return x - y
}

// Multiplies two integers
function multiply(x,y) {
  return x * y
}

// Divides two integers.
function divide(x,y) {
  return x / y
}

// Increments
function inc(n) {
  return ++n
}

// decrements.
function dec(n) {
  return --n
}

// Parses (n) as an integer and returns the parsed integer.
function makeInt(n) {
  return parseInt(n, 10)
}

// Perseves (n) as a decimal and returns the parsed number.
function preserveDecimal(n) {
  return parseFloat(n)
}
