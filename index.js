function add(a, b) {
  return a + b;
}

function subtract(c, d) {
  return c - d;
}

function multiply(e, f) {
  return e * f;
}

function divide(g, h) {
  return g / h;
}

add(1, 80)
subtract(60, 40)
multiply(2 * 3.4)
divide(5.0 / 2.5)

function inc(n) {
  return ++n;
}
function dec(n) {
  return --n;
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
