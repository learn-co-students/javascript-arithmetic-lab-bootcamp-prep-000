function add(number1, number2) {
  return (number1 + number2)
}

function subtract(number1, number2) {
 return (number1 - number2)
}

function multiply(number1, number2) {
 return (number1 * number2)
 }

function divide(number1, number2) {
 return (number1 / number2)
 }

function inc(n) {
return (n += 1)
}

function dec(n) {
return (n -= 1)
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}