
function add(a,b) {
  // a function to add two numbers
  return a+b
}

function subtract(a,b) {
  // a function to subtract two numbers
  return a-b
}

function multiply(a,b) {
  // a function to multiply two numbers
  return a*b
}

function divide(a,b) {
  // a function to divides two numbers
  return a/b
}

function inc(n) {
  // a function that increments and returns n
  return n += 1
}

function dec(n) {
  // a function that decrements and returns n
  return n -= 1
}

function makeInt(n) {
  // a function that decrements and returns n
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  // a function that preserves n's decimals (it parses n as a floating point number) and returns the parsed number
  return parseFloat(n,10)
}
