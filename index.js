function add(a, b) {
  var sum = a + b
  return sum
}

function subtract(a, b) {
  var sum = a - b
  return sum
}

function multiply(a, b) {
  var product = a * b
  return product
}

function divide(a, b) {
  var product = a / b
  return product
}

function inc(n) {
return ++n
}

function dec(n) {
  return --n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
