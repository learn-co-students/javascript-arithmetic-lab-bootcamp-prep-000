function add(a, b) {
var add = a + b;
return add;
}
function subtract(a, b) {
  var subtract = a - b;
  return subtract;
}
function multiply(a, b) {
  var multiply = a * b;
  return multiply;
}
function divide(a, b) {
  var divide = a / b;
  return divide;
}
function inc(n) {
  var inc = n;
  inc++
  return inc;
}
function dec(n) {
  var dec = n;
  dec--
  return dec;
}
function makeInt(n){
  return parseInt(n, 10)
}
function preserveDecimal(n) {
  return parseFloat(n)
}
