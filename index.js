function add(first, seconde) {
  return first + seconde
}

function subtract(a, b) {
  return a - b
}
function multiply(a, b) {
  return a * b
}
function divide(a, b) {
  return a/b
}
function dec(a) {
  return a - 1
}
function inc(a) {
  return a + 1
}
function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}