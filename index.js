function add(a,b) {
  return parseInt(a, 10) + parseInt(b, 10)
}
function subtract(a,b) {
  return parseInt(a, 10) - parseInt(b, 10)
}
function multiply(a,b) {
  return parseInt(a, 10) * parseInt(b, 10)
}
function divide(a,b) {
  return parseInt(a, 10) / parseInt(b, 10)
}
function inc(n) {
  n++
  return n
}
function dec(n) {
  n--
  return n
}
function makeInt(n) {
  return parseInt(n, 10)
}
function preserveDecimal(n) {
  return parseFloat(n)
}