function add(x, y) {
  return x + y
}
function subtract(x, y) {
  return x - y
}
function multiply(x, y) {
  return x * y
}
function divide(x, y) {
  return x/y
}
function inc(n) {
  n++
  return n
}
function dec(n) {
  n--
  return n
}
function makeInt(x) {
  return parseInt(x, 10)
}
function preserveDecimal(x) {
  return parseFloat(x)
}