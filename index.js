var add = function(a, b){
  var result = a + b;
  return result;
}
var subtract = function(a, b){
  var result = a - b;
  return result;
}
var multiply = function(a, b){
  var result = a * b;
  return result;
}
var divide = function(a, b){
  var result = a / b;
  return result;
}
var inc = function(n){
  n++;
  return n;
}
var dec = function(n){
n--;
return n;
}
var makeInt = function(n){
  var r = parseInt(n, 10);
  return r;
}
var preserveDecimal = function(n){
  var r = parseFloat(n);
  return r;
}
