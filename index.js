function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  var number = (n)
  return number += 1
}

function dec(n) {
  var number = (n)
  return number -= 1
}

function makeInt(n) {
  var number = (n)
  // return parseInt(number)
  return parseInt(number, 10)
  return parseInt('hello')
}

function preserveDecimal(n) {
  var number = (n)
  return parseFloat(number)
  return parseFloat('qwerty')
}
