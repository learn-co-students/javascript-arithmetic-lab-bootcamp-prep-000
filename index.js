const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

const inc = (a) => {
  return a += 1;
};

const dec = (a) => {
  return a -= 1;
};

const makeInt = (n) => {
  return parseInt(n, 10);
};

const preserveDecimal = (n) => {
  return parseFloat(n);
};
