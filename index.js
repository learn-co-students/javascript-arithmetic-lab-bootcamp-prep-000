function add(a, b){
  return a+b;
}

function subtract(a, b){
  return a-b;
}

function divide(a, b){
  return a/b;
}

function multiply(a, b){
  return a*b;
}

function inc(num){
  return num+=1;
}

function dec(num){
  return num-=1;
}

function makeInt(str){
  return parseInt(str, 10);
}

function preserveDecimal(num){
  return parseFloat(num);
}