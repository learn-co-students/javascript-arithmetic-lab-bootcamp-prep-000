function add(a, b) {
return a+b
}
function subtract(a,b){
return a-b
}
function multiply(a,b){
return a*b
}
function divide(a, b) {
return a/b
}
function inc(n){
var increment = n +=1
return increment
}
function dec(n){
var decrement = n-=1
return decrement
}
function makeInt(n){
return parseInt(n,10)
}

function preserveDecimal(n){
 return parseFloat(n)
}
