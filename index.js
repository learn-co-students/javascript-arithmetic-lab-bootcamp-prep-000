function add(a, b) {
  var number = a + b
  return number
}

function subtract(b, a) {
  var number = b - a 
  return number
}

function multiply(a, b) {
  var number = a * b 
  return number 
}

function divide(a, b) {
  var number = a / b 
  return number  
}

function inc(n) {
  var number = n += 1 
  return number 
}

function dec(n) {
  var number = n -= 1
  return number
}

function makeInt(n) {
  var number = parseInt(n)
  return number
}

function makeInt(n) {
  var number = parseInt(n, 10)
  return number
}

function preserveDecimal(n){
  var number = parseFloat(n)
  return number
}
