function add(a, b)
{
  var sum = a + b
return sum 
}

function subtract(a, b)
{
  var difference = a - b
return difference 
}

function multiply(a, b)
{
  var product = a * b
return product 
}

function divide(a, b)
{
  var quotient = a / b
return quotient 
}

function inc(a)
{
  var increment = ++a
return increment 
}

function dec(a)
{
  var decrement = --a
return decrement 
}

function makeInt(a)
{
  var parsed = parseInt(a, 10)
return parsed 
}

function preserveDecimal(a)
{
  var float = parseFloat(a, 10)
return float 
}