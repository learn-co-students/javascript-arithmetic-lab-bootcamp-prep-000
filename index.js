//1.add a and b
function add(a,b) {
  return a+b
}

//2.subtarcts b from a
function subtract(a,b) {
  return a-b
}

//3.multiplies a and b 
function multiply(a,b) {
  return a*b
}

//4.divides a by b
function divide(a,b) {
  return a/b
}

//5.adds 1 to n
function inc(n) {
  return n+1
}

//6.subtracts 1 from n
function dec(n) {
  n -= 1
  return n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}