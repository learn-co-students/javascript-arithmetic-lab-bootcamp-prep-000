function add(a, b){
  return a + b;
}

function subtract(a, b){
    return a - b;
}

function multiply(a, b){
    return a * b;
}

function divide(a, b){
    return a/b;
}

function inc(n){
var inc = n +1;
  return inc;
}

function dec(n){
  var dec = n - 1;
  return dec;
}

function makeInt(n){
var n = parseInt(n, 10);
return n;
}

function preserveDecimal(n){
return parseFloat(n);
}

