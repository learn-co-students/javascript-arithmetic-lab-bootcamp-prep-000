function add(a,b) {
  return a + b;
}
function subtract(a,b) {
  return a - b;
}
function multiply(a,b) {
  return a * b;
}
function divide(a,b) {
  return a / b;
}
function inc(a) {
  a += 1;
  return a;
}
function dec(a) {
  a -= 1;
  return a;
}
function makeInt(a) {
  return parseInt(a,10);
}


function preserveDecimal(n) {
  return parseFloat(n);
}
