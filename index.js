function add(a,b){
  return a+b
}

function subtract(a,b){
  return a-b
}

function multiply(x,y){
  return x*y
}

function divide(x,y){
  return x/y
}

function inc(n){
  n++;
  return n;
}


function dec(n){
  n--;
  return n;
}

function makeInt(n) {
  return parseInt(n)
}


function makeInt(n){
  return parseInt(n, 10);
}
  
  
function preserveDecimal(n){
  return parseFloat(n);
}
