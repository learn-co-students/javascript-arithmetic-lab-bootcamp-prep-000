function add(a,b) {
  var total = a + b;
  return total;
}

function subtract(a,b) {
  var total = a - b;
  return total;
}

function multiply(a,b) {
  var total = a * b;
  return total;
}

function divide(a,b) {
  var total = a / b;
  return total;
}

function inc(n) {
  n++;
  return n;
}

function dec(n) {
  n--;
  return n;
}

function makeInt(n) {
  var a = parseInt(n, 10);
  return a;
}

function preserveDecimal(n) {
  var a = parseFloat(n);
  return a;
}
