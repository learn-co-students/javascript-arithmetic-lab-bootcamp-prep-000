
//Function will add a and b
function add (a, b) {
    return a + b
}
//Function will subtract a and b
function subtract (a, b){
  return a - b
}
//Function will multiply a and b
function multiply (a, b){
  return a * b
}
//Function will divide a and b
function divide(a, b){
  return a / b
}
//Function will increment a by one (could've also used a++)
function inc(a){
  return (a+1)
}
//Function will decrement a by one (could've also used a--)
function dec(a){
  return (a-1)
}
//Function makes sure a is a whole number base 10
function makeInt (a){
  return parseInt (a, 10)
}
//function will preserve a decimal number
function preserveDecimal (a) {
  return parseFloat (a)
}
