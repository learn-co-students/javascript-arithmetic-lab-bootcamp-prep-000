function add(a,b){
  return a+b;
}
function subtract(a,b){
  return a-b;
}
function multiply(a,b){
  return a*b;
}
function divide(a,b){
  return a/b;
}
function inc(n){
  return n+1;
}
function dec(n){
  return n-1;
}
function makeInt(n){
  return parseInt(n);
}
function makeInt(n,b){
  b=10;
  return parseInt(n,b);
}
function preserveDecimal(n){

  return parseFloat(n);
}