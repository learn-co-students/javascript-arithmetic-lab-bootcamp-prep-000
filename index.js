function add(first, second) {
  return first + second
}

function subtract(left, right) {
  return left - right
}

function multiply(first, second) {
  return first * second
}

function divide(left, right) {
  return left / right
}

function inc(number) {
  return ++number
}

function dec(number) {
  return --number
}

function makeInt(something) {
  return parseInt(something, 10)
}

function preserveDecimal(something) {
  return parseFloat(something)
}
