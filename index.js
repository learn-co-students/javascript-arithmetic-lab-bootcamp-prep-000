function add(a,b) {
  var sum = a+b 
  return sum 
}

function subtract(a,b) {
  var sum = a-b
  return sum
}

function multiply(a,b) {
  var sum = a*b 
  return sum
}

function divide(a,b) {
  var sum = a/b 
  return sum
}

function inc(a) {
  var increment = ++a
  return increment
}

function dec(a) {
  var decrement = --a
  return decrement 
}

function makeInt(a) {
  var parsed = parseInt(a, 10)
  return parsed
}

function preserveDecimal(a) {
  var decimal = parseFloat(a, 10)
  return decimal
}