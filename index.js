function add(a, b) {
  var a, b;
  return a + b;
}

function subtract(a, b) {
  var a, b;
  return a - b;
}

function multiply(a, b) {
  var a, b;
  return a * b;
}

function divide(a, b) {
  var a, b;
  return a / b;
}

function inc(n) {
  var n;
  return ++n;
}

function dec(n) {
  var n;
  return --n;
}

function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  parseFloat(n);
  return n;
}
