function add(a,b){
  return parseInt(a)+parseInt(b);
}

function subtract(a,b){
  return parseInt(a)-parseInt(b);
}

function multiply(a,b){
  return parseInt(a)*parseInt(b);
}

function divide(a,b){
  return parseInt(a)/parseInt(b);
}

function inc(a){
  return parseInt(a)+1;
}

function dec(a){
  return parseInt(a)-1;
}

function makeInt(a){
  return parseInt(a);
}

function makeInt(a){
  return parseInt(a,10);
}

function preserveDecimal(a){
  return parseFloat(a);
}







