function add(a, b) {
  var a = a
  var b = b
  return a + b
}

function subtract(a, b) {
  var a = a
  var b = b
  return a - b
}

function multiply(a, b) {
  var a = a
  var b = b
  return a * b
}

function divide(a, b) {
  var a = a
  var b = b
  return a / b
}

function inc(n) {
  var n = n
  n++
  return n
}

function dec(n) {
  var n = n
  n--
  return n
}

function makeInt(n) {
  var n = n
  var parsedN = parseInt(n, 10)
  return parsedN
}

function preserveDecimal(n) {
  var n = n
  var parsedN = parseFloat(n)
  return parsedN
}
