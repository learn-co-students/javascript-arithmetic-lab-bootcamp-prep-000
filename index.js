// add
function add(a, b) {
   return a + b;
}

// subtract
function subtract (a, b){
    return a - b;
}

// multiply
function multiply (a , b) {
    return a * b;
}

// divide
function divide (a, b){
    return a / b;
}

function inc(c) {
    return ++c;
}

function dec(c) {
    return c -= 1;
}

function makeInt(n) {
    var n = parseInt(n, 10);
    return n;
}

function preserveDecimal(n) {
    return parseFloat(n);
}
