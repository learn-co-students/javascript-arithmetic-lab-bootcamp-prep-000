function add(a,b) {
  var add = a + b
  return add
}

function subtract(a,b) {
  var subtract = a - b
  return subtract
}

function multiply(a,b) {
  var multiply = a * b
  return multiply
}

function divide(a,b) {
  var divide = a / b
  return divide
}
function inc(a){
  var increment = a + 1
  return increment++
}

function dec(a){
  var increment = a - 1
  return increment--
}

function makeInt(a){
  return parseInt(a, 10)
}

function preserveDecimal(a){
  return parseFloat (a, 10)
}
