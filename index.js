function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(a) {
  return a + 1
}

function dec(a) {
  return a - 1
}

function makeInt(string) {
  return parseInt(string, 10)
}

function preserveDecimal(b) {
  return parseFloat(b)
}