function add(a, b){
  return a + b // add
}
function subtract(a, b){
  return a - b // subtract
}
function multiply(a, b){
  return a * b // multiply
}
function divide(a, b){
  return a / b // divide
}
function inc(a){
  return a += 1
}
function dec(a){
  return a -= 1
}
function makeInt(a){
  var x = parseInt(a, 10)
  return x
}
function preserveDecimal(a){
  var x = parseFloat(a, 10)
  return x
}
