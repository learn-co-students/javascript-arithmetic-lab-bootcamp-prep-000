function add(a, b) {
  var sum;
  sum = a + b;

  return sum;
}
function subtract(a, b) {
  var difference;
  difference = a - b;

  return difference;
}
function multiply(a, b) {
  var product;
  product = a * b;

  return product;
}
function divide(a, b) {
  var quotient;
  quotient = a / b;

  return quotient;
}
function inc(n) {
  n++;
  return n;
}
function dec(n) {
  n--;
  return n;
}
function makeInt(n) {
  return parseInt(n, 10);
}
function preserveDecimal(n) {
  return parseFloat(n);
}
