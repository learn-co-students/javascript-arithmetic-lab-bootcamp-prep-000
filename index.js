function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

 function multiply(a, b){
   return a * b
 }

 function divide(a, b){
   return a / b
 }

function inc(n){
  return ++n
}

function dec(n) {
  return --n
}

function makeInt(n){
  parseInt('2', 10)
  return n
}

 function preserveDecimal(n){
   parseFloat(2.22)
   return n
 }
