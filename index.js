"use strict";
function add(a, b) {
    return a + b;
}

"use strict";
function subtract(a, b) {
    return a - b;
}

"use strict";
function multiply(a, b) {
    return a * b;
}

"use strict";
function divide(a, b) {
    return a / b;
}

"use strict";
function inc(a) {
    return a += 1;
}

"use strict";
function dec(a) {
    return a -= 1;
}

"use strict";
function makeInt(n) {
    return parseInt(n, 10);
}

"use strict";
function preserveDecimal(n) {
    return parseFloat(n);
}
