function add(a, b){
  return a + b;
}

function subtract(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function inc(x){
  return x+= 1;
}

function dec(x){
  return x-= 1;
}

function makeInt(x){
  return parseInt(x, 10);
}

function preserveDecimal(x){
  return parseFloat(x);
}
