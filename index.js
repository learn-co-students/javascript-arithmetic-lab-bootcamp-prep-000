var add = (a, b) => {
  return a+b;
}
var subtract = (a, b) => {
  return a-b;
}
var multiply = (a, b) => {
  return a*b;
}
var divide = (a, b) => {
  return a/b;
}
var inc = (n) => {
  return ++n;
}
var dec = (n) => {
  return --n;
}
var makeInt = (n) => {
  return parseInt(n, 10);
}
var preserveDecimal = (n) => {
  return parseFloat(n);
}
