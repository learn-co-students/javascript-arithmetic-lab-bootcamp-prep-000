function add(a, b) {
  return a + b;
}
add (5, 2);

function subtract(a, b) {
  return a - b;
}
subtract (52 - 21);

function multiply(a, b) {
  return a * b;
}
multiply (865 * 432);

function divide(a, b) {
  return a / b;
}
divide (432 / 51);

function inc(n) {
  return ++n;
}
inc (23);

function dec(n) {
  return --n;
}
dec (32);

function makeInt(n) {
  return parseInt(n);
}

function makeInt(n) {
  return parseInt(n, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
