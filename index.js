function add(a,b){
  var sum=a+b;
  return sum;
}
function subtract(a,b){
  var difference=a-b;
  return difference;
}
function multiply(a,b){
  var mul=a*b;
  return mul;
}
function divide(a,b){
  var div=a/b;
  return div;
}
function inc(n){
  var increment=++n;
  return increment;
}
function dec(n){
  var decrement=--n;
  return decrement;
}
function makeInt(n){
  var integer=parseInt(n);
  return integer;
}
function makeInt(n){
  var integer=parseInt(n,10);
  return integer;
}
function preserveDecimal(n){
  var integer=parseFloat(n,10);
  return integer;
}