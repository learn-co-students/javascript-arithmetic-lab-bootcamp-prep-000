function add(a, b) {
  var result = a + b
  return result
}
function subtract(a, b) {
  var result = a - b
  return result
}
function multiply(a, b) {
  var result = a * b
  return result
}
function divide(a, b) {
  var result = a / b
  return result
}
function inc(n) {
  var result = ++n
  return result
}
function dec(n) {
  return --n
}
function makeInt(n) {
  return parseInt(n, 10)
}
function preserveDecimal(n) {
  return parseFloat(n)
}