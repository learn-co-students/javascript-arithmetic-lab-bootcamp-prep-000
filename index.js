function add(a,b){
  return a+b;
}

function subtract(a,b){
  return a-b;
}

function multiply (a,b){
  return a*b;
}

function divide (a,b){
  var divide= a/b;
  return divide;
}

function inc(n){
  return ++n;
}

function dec(n){
  return --n;
}

function makeInt(n){
  return parseInt(n,10);
}

function preserveDecimal(n){
  return parseFloat(n);
}
