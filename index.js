function add(a, b) {
 return a + b
}

function subtract(a, b) {
 return a - b
}

function multiply(a, b) {
 return a * b
}

function divide(a, b) {
 return a / b
}

function inc(n) {
  var number  = n
  number++
  return number
}

function dec(n) {
  var number = n
  number--
  return number
}

function makeInt(n) {
  var integer = parseInt(n, 10)
  return integer 
}

function preserveDecimal(n) {
  var float = parseFloat(n)
  return float
}