function add(a,b){
  return a+b
}

function preserveDecimal(n){
  return parseFloat(n)
}

function makeInt(n){
  return parseInt(n, 10)
}

function dec(n){
  return --n
}

function inc(n){
  return ++n
}

function divide(a,b){
  return a/b
}

function multiply(a,b){
  return a*b
}

function subtract(a,b){
  return a-b
}