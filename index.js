function add(a,b) {
  var result = a + b
  return result
}

function subtract(a, b) {
  var result = a - b
  return result
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(n) {
  return n + 1
}

function dec(n) {
  return n - 1
}

function makeInt(n) {
  return parseInt(n)
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}

