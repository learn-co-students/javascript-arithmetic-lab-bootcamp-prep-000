//ADD
function add(a, b) {
return a + b;
}

//subtract
function subtract(a, b) {
  return a - b;
}

//MULTIPLY
function multiply(a, b) {
  return a * b;
}

//DIVISION
function divide(a, b) {
  return a/b;
}

//increments
function inc(n) {
  return ++n;
}

//decrements
function dec(n) {
  return --n;
}

//Parse Integer
function makeInt(n) {
  return parseInt (n, 10)
  ;
}

//PARSE FLOAT
function preserveDecimal(n) {
  return parseFloat(n);
}
