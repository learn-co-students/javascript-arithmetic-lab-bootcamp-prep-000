function add(a, b) {
  return parseInt(a, 10) + parseInt(b, 10);
}

function subtract(a, b) {
  return parseInt(a, 10) - parseInt(b, 10);
}

function multiply(a, b) {
  return parseInt(a, 10) * parseInt(b, 10);
}

function divide(a, b) {
  return parseInt(a, 10) / parseInt(b, 10);
}

function inc(a) {
  return ++a;
}

function dec(a) {
  return --a;
}

function makeInt(a) {
  return parseInt(a, 10);
}

function preserveDecimal(n) {
  return parseFloat(n);
}
