// Function 1. Add

function add(a,b)  {
  return a + b
}

// Function 2. Subtract

function subtract(a,b) {
  return a - b
}

// Function 3. Multiply

function multiply(a,b) {
  return a * b
}

// Function 4. Divide

function divide(a,b) {
  return a / b
}

// Function 5. Inc

function inc(n) {
  return n += 1
}

// Function 6. Dec

function dec(n) {
  return n -= 1
}

// Function 7. makeINt

function makeInt(n) {
  return parseInt(n, 10)
}

// Function 8. preserveDecimal

function preserveDecimal(n) {
  return parseFloat(n)
}
