function add (a,b)
{
  return(a+b);
}

function subtract (a,b)
{
  return(a-b);
}

function multiply(a,b)
{
  return(a*b);
}

function divide (a,b)
{
  return(a/b);
}
function inc (n)
{
  return (++n);
}
function dec (n)
{
  return (--n);
}

function makeInt(number)
{
  return(parseInt(number,10));
}

function preserveDecimal(number)
{
  return(parseFloat(number));
}
