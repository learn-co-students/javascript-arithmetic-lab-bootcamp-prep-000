function add(firstNum, secNum) {
return firstNum + secNum
}
function subtract(firstNum, secNum) {
return firstNum - secNum
}
function multiply(firstNum, secNum) {
return firstNum * secNum
}
function divide(firstNum, secNum) {
return firstNum / secNum
}

function inc(n) {
  return ++n
}

function dec(n) {
  return --n
}

function makeInt(n) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
