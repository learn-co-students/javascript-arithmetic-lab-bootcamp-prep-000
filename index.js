function add (num, num2) {
  return num + num2;
}

function subtract (a, b) {
  return a - b;
}

function multiply (num, num2) {
  return num * num2;
}

function divide (a, b) {
  return a / b;
}

function inc (n) {
  return n += 1;
}

function dec (n) {
  return n -= 1;
}

function makeInt (n) {
  return parseInt(n, 10);
}

function preserveDecimal (n) {
  return parseFloat(n, 10);
}
