function add(a,b){
  var x = a+b
  return x
}

function subtract(a,b){
  var x = a-b
  return x
}

function multiply(a,b){
  var x = a*b
  return x
}

function divide(a,b){
  var x = a/b
  return x
}

function inc(n){
  n++
  return n
}

function dec(n){
  n--
  return n
}

function makeInt(n){
  var x = parseInt(n, 10);
  return x
}

function preserveDecimal(n){
  let v = parseFloat(n);
  return v;
}
