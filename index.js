function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

//inc(n) increments n and returns the result

function inc(n) {
  n++
  return n
}

function dec(n) {
  n--
  return n
}

function makeInt(n, x) {
  return parseInt(n, 10)
}

function preserveDecimal(n) {
  return parseFloat(n)
}

