function add(a,b){
  var c;
  c = a + b ;
  
  return c
}

function subtract(a,b){
  var c;
  c = a - b ;
  
  return c
}

function multiply(a,b){
  var c;
  c = a * b ;
  
  return c
}

function divide(a,b){
  var c;
  c = a / b ;
  
  return c
}

function inc(a){
  var c;
  c = a += 1 ;
  
  return c
}

function dec(a){
  var c;
  c = a -= 1 ;
  
  return c
}

function makeInt(n){
  var c;
  c = parseInt(n, 10); 
  return c
}

