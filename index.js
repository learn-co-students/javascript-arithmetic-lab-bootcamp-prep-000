function add(a, b){
  var sum = a + b
  return sum
}

function subtract(a, b){
  var difference = a - b
  return difference
}

function multiply(a, b){
  return a*b
}

function divide(a, b){
  return a/b
}

function inc(n){
  return ++n
}

function dec(n){
  return --n
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}