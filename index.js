function add(x,y){
  return x+y
}

function subtract(x,y){
  return x-y
}

function multiply(x,y){
  return x*y
}

function divide(x,y){
  return x/y
}

function increment(n){
  return n+=1
}

function decrement(n){
  return n-=1
}

function makeInt(s){
  return parseInt(s,10)
}

function preserveDecimal(s){
  return parseFloat(s)
}
