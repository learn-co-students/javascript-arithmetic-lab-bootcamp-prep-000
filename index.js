function add(number1, number2){
  var sum = number1 + number2
  return sum
}


function subtract(a,b){
  return a-b
}


function multiply(a,b){
  return a*b
}


function divide(a,b){
  return a/b
}


function inc(n){
  return ++n
}


function dec(n){
  return --n
}


function makeInt(n){
  return parseInt(n,10)
}

function preserveDecimal(n){
  return parseFloat(n)
}

