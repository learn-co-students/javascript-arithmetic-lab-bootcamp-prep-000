// Addition Function
function add(a, b) {
  return a + b
}

// Subtraction Function
function subtract(a, b){
  return a - b
}

// Multiplication Function
function multiply(a, b){
  return a * b
}

// Division Function
function divide(a, b){
  return a / b
}

// Incriment Function
function inc(n){
  return n += 1
}

// Decrease Function
function dec(n) {
  return n -= 1
}

// Integer Function
function makeInt(n){
  return parseInt(n, 10)
}

// Float Function
function preserveDecimal(n){
  return parseFloat(n)
}
