function add(a, b) {
  return a + b;
}


function subtract(a, b) {
  return a - b;
}


function multiply(a, b) {
  return a * b;
}


function divide(a, b) {
  return a / b;
}


function inc(n) {
  return n += 1;
}


function dec(n) {
  return n -= 1;
}



function makeInt(n) {
  return parseInt(n, 10);
}
console.log(makeInt(8.888));
console.log(makeInt("blabla"));
   
   
   
function preserveDecimal(n) {
  return parseFloat(n);
}
console.log(preserveDecimal(2.22));
console.log(preserveDecimal('hsdfhsdfkj'));









