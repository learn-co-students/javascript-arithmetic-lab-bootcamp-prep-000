function add(a,b){
  return parseInt(a)+parseInt(b)
}

function subtract(a,b){
  return parseInt(a)-parseInt(b)
}

function multiply(a,b){
  return parseInt(a)*parseInt(b)
}

function divide(a,b){
  return parseInt(a)/parseInt(b)
}

function inc(a){
  var x=parseInt(a)
  x++
  return x
}

function dec(a){
  var x=parseInt(a)
  x--
  return x
}

function makeInt(a){
 return parseInt(a,10)
}

function preserveDecimal(a){
  return parseFloat(a)
}

