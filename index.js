function add(a=5, b=6) {
  return a + b;
}

function subtract(a=5, b=6) {
  return a - b;
}

function multiply(a=5, b=6) {
  return a * b;
}

function divide(a=5, b=6) {
  return a / b;
}

function inc(n){
 return n += 1;
}

function dec(n){
 return n -= 1;
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}