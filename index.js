// adding Function
function add(a, b)
{
  return a + b
}

// subtracting function
function subtract(a, b)
{
  return a - b
}

// multiplying function
function multiply(a, b)
{
  return a * b
}

// dividing function
function divide(a, b)
{
  return a / b
}

// incrementing function
function inc(a)
{
  a++
  return a
}

// decrementing function
function dec(a)
{
  a--
  return a
}

// parsing integers from var
function makeInt(a)
{
  return parseInt(a, 10)
}

// preserving decimals
function preserveDecimal(a)
{
  return parseFloat(a)
}

